/*
    var 
    Variáveis declaradas com var têm escopo global ou de função 
    Podem ser atualizadas e declaradas novamente dentro do seu escopo
    Existem desde o começo na linguagem javascript

*/

var variavelVar = "Diego";
console.log(variavelVar);


var variavelVar = "Maria";
console.log(variavelVar);


var variavelVar = 'Fabio';
console.log(variavelVar);

/*
    let
    Variáveis decladas com let têm escopo em bloco
    Podem ser atualizadas, mas não declarada novamente
    Foram criadas posteriormente para evitar confusão
*/

let variavelLet = 10
console.log(variavelLet)


variavelLet = 33
console.log(variavelLet)


// const
// Constantes = o valor não pode ser alterado

const constanteConst = true
console.log(constanteConst)

constanteConst = false

console.log(constanteConst)


