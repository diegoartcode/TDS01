// 2 - Número positivo ou negativo
// Peça ao usuário um número e diga se ele é positivo ou negativo.
// O número é positivo
// O número é negativo.


let numero = parseFloat(prompt("Digite um número: "));

if(numero >=0 ){
    console.log("O número é positivo")
}else{
    console.log("O número é negativo")
}